#Calcularora de consumo de distancia que foi consumida a gasolina

DistPercorrida = 300.0  # Distância percorrida em km
CombustivelGasto = 25.0  # Combustível gasto em litros
ConsumoMedio = DistPercorrida / CombustivelGasto  # Consumo médio em km/l
print(f"Para percorrer {DistPercorrida:.2f} km, foram gastos {CombustivelGasto:.2f} litros de combustível e o consumo médio foi de {ConsumoMedio:.2f} km/l")